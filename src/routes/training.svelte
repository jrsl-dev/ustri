<section>
  <h3>Equipment</h3>

  <p>
    The equipment used depends on the experience level of the particular
    student.
  </p>

  <p>
    Students new to our organization are simply required to wear clothing that
    will allow them to move around with ease. Loaner bokuto (wooden sword) and
    obi (belt) will be provided when first starting out.
  </p>

  <p>
    Ungraded students that have become deshi wear a standard Karate/Judo style
    gi and will be begin using iaito (an alloy based sword that is unable to
    hold an edge).
  </p>

  <p>
    Graded students wear white iai style uwagi (a short sleeve top) with kaku
    obi and black hakama (pleated trouser). Instructors will typically wear the
    same as graded students but with a black uwagi, unless it is a formal
    occasion.
  </p>

  <p>
    In all cases, knee pads are <strong>highly recommended</strong>. Blades that
    have an edge or might be able to hold an edge are never allowed during
    class.
  </p>

  <p>
    <strong>All</strong> equipment must be inspected by the instructor before being
    used by a student in class.
  </p>
