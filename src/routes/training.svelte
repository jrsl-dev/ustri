<section>
  <h2>Training Information</h2>

  <p>
    Iaijustu is are techniques of drawing and cutting with the Japanese sword,
    or katana. Tamiya Ryu is a specific school of iaijutsu which is described as
    both a Martial Art (bujutsu) and a Martial Way (budo).
  </p>

  <p>
    Tamiya Ryu Iaijutsu is distinguished by its emphasis on practicing basic
    techniques (Kihon). In other words, to properly learn kata means studying in
    great detail: 1) how to cut using the scabbard opening (Saya-guchi) and 2)
    how to use the scabbard hand (Saya-te). As a serious course of study,
    learning Tamiya Ryu Iaijutsu involves having a clear understanding of the
    laws known as “Tamiya Nobility and Tamiya Purity.”
  </p>

  <p>Practice is conducted in a non-competitive environment.</p>
</section>

<section>
  <h3>Kata</h3>

  <p>
    As a koryu (a school established before the Meiji Restoration) the primary
    mode of training is kata. Kata are prearranged forms of practice design to
    instill the princles of the art into the student through repeated pratice.
    Iaijutsu kata are performed solo and usually consist of three phases:
  </p>

  <ol>
    <li>Nukitsuke - The initial sword draw</li>
    <li>Kiritsuke - A follow up strike</li>
    <li>Noutou - Resheathing the blade</li>
  </ol>

  <p>
    The techniques of Tamiya Ryu are taught in two volumes. The kata are
    followed by a third volume of paired techniques only taught once the student
    has completed their initial study of the solo kata.
  </p>

